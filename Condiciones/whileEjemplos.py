#Creamos un programa que no caiga en un bucle infinito

numerillo = int(1)

while(numerillo <= 10): #Mientras el numero sea menor o igual a 10, seguirá ejecutando el código
    print("El valor del numero es: ", numerillo) #Imprime el valor del numero
    numerillo = numerillo + 1 #Incrementa el numero en 1

